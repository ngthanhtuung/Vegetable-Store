package data.dto;

/**
 *
 * @author TungNT
 */
public class CategoryDTO {
    private String id;
    private String title;

    public CategoryDTO() {
        this.id = "";
        this.title = "";
    }

    public CategoryDTO(String id, String title) {
        this.id = id;
        this.title = title;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    
}
